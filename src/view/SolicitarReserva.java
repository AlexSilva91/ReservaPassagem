    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FachadaReserva reserva = new FachadaReserva(MAX);
        Reserva resv = new Reserva();
        GerarCodigo codigo = new GerarCodigo();
        try {
            resv.setEmpresa((String) empresaBox.getSelectedItem());
            resv.setDataReserva(txtData.getText());
            resv.setHorario(txtHora.getText());
            resv.setCod(codigo.gerarCodigo());
            reserva.SolicitarReserva(resv);
            JOptionPane.showMessageDialog(rootPane, "Reserva solicitada!\nSeu protocolo Ã© "
                    +resv.getCod()+"!\nGuarde-o para eventuais consultas!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

