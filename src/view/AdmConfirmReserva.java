    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        FachadaReserva fachadaReserva = new FachadaReserva(MAX);
        Reserva reserva = new Reserva();
        try {
            if ("Solicitado".equals(txtStatus.getText())) {
                reserva.setStatus("Aprovado");
                reserva.setCod(Integer.parseInt(txtCod.getText()));
            }
            fachadaReserva.ConfirmReserva(reserva);
            JOptionPane.showMessageDialog(null, "Reserva aprovada!");
            PreencherTabela();
        } catch (SQLException | ReservaNaoEncontrada | NaoHaReservas ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

