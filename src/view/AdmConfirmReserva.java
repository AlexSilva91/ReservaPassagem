    public final void PreencherTabela() throws SQLException, NaoHaReservas {
        try {
            this.conn = ConnectionFactory.getConnection();
            String sql = "SELECT * FROM reserva";
            PreparedStatement stmt = conn.prepareStatement(sql);
            try (ResultSet resultSet = stmt.executeQuery()) {
                DefaultTableModel modelo = (DefaultTableModel) table.getModel();
                modelo.setNumRows(0);
                while (resultSet.next()) {
                    modelo.addRow(new Object[]{resultSet.getString("id_reserva"),
                        resultSet.getString("dataReserva"), resultSet.getString("horario"),
                        resultSet.getString("status"), resultSet.getString("empresa"),
                        resultSet.getString("cod")});
                }
            }
            this.conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }

